# PROCESAMIENTO PUNTO 1: Tiempos de Atención por Cajero
print("\n[RESPUESTA PUNTO 1] Estadísticas de Atención por Máquina (Fase Estable):")
medias_atencion = {}
for c, datos in cajeros_resultados.items():
    servicios_puros = datos['Servicio'][punto_corte:]
    media = np.mean(servicios_puros)
    medias_atencion[c] = media
    desv = np.std(servicios_puros, ddof=1)
    minimo = np.min(servicios_puros)
    maximo = np.max(servicios_puros)

    # Intervalo de confianza del 95% para la media
    ic_inf, ic_sup = stats.t.interval(0.95, len(servicios_puros)-1, loc=media, scale=stats.sem(servicios_puros))

    print(f"  * {c}:")
    print(f"   - Tiempo Promedio de Atención: {media:.3f} minutos")
    print(f"   - Desviación Estándar: {desv:.3f} min | Rango: [{minimo:.2f} - {maximo:.2f}] min")
    print(f"   - Intervalo de Confianza 95%: [{ic_inf:.3f} , {ic_sup:.3f}]")

# ADICIONAL: Cajero con menor y mayor tiempo promedio de atención
cajero_min = min(medias_atencion, key=medias_atencion.get)
cajero_max = max(medias_atencion, key=medias_atencion.get)
print("\n[ADICIONAL] Comparativa de Cajeros:")
print(f"   - Cajero con MENOR tiempo promedio de atención: {cajero_min} ({medias_atencion[cajero_min]:.3f} minutos)")
print(f"   - Cajero con MAYOR tiempo promedio de atención: {cajero_max} ({medias_atencion[cajero_max]:.3f} minutos)")

# PROCESAMIENTO PUNTO 2: Distribución de Usuarios
print("\n[RESPUESTA PUNTO 2] Distribución de Usuarios en la Totalidad de Cajeros:")
total_clientes_analizados = 0
conteos_usuarios = np.zeros(4)

for c, datos in cajeros_resultados.items():
    tipos_puros = datos['Tipos'][punto_corte:]
    total_clientes_analizados += len(tipos_puros)
    for t in tipos_puros:
        conteos_usuarios[t] += 1

for i in range(4):
    porcentaje_obtenido = (conteos_usuarios[i] / total_clientes_analizados) * 100
    print(f"   - Tipo [{NOMBRES_TIPOS[i].upper()}]: Esperado {PROBS[i]*100:.1f}% | Simulado Real: {porcentaje_obtenido:.2f}% ({int(conteos_usuarios[i])} clientes)")

# ADICIONAL: Promedio de usuarios de cada tipo en la totalidad de cajeros
promedio_usuarios_por_tipo = conteos_usuarios / num_cajeros
print("\n[ADICIONAL] Promedio de usuarios de cada tipo por cajero (fase estable):")
for i in range(4):
    print(f"   - Tipo [{NOMBRES_TIPOS[i]}]: promedio de {promedio_usuarios_por_tipo[i]:.1f} usuarios por cajero")

# 5. GRÁFICA DE VALIDACIÓN CON LA TEORÍA M/M/1

# Gráfica adicional para sustentar el punto 4 (Proceso de Validación)
plt.figure(figsize=(10, 5))
