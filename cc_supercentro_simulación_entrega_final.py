import numpy as np
import matplotlib.pyplot as plt
import scipy.stats as stats

np.random.seed(42)

# 1. PARAMETRIZACIÓN DEL MODELO (CALIBRACIÓN)

PROBS = [0.25, 0.20, 0.275, 0.275]  # Rápido, Normal, Lento, Muy Lento
TIEMPOS_SERVICIO = [1.0, 3.0, 4.0, 6.0]
TIEMPOS_LLEGADA = [3.0, 3.0, 5.0, 7.0]
NOMBRES_TIPOS = ['Rápido', 'Normal', 'Lento', 'Muy Lento']

# Cálculo de parámetros globales compuestos (Análisis Teórico)
t_servicio_ponderado = sum(p * t for p, t in zip(PROBS, TIEMPOS_SERVICIO))
t_llegada_ponderado = sum(p * t for p, t in zip(PROBS, TIEMPOS_LLEGADA))

lambda_global = 1.0 / t_llegada_ponderado
mu_global = 1.0 / t_servicio_ponderado
num_cajeros = 3
lambda_por_cajero = lambda_global / num_cajeros

print("="*50)
print("     CALIBRACIÓN INICIAL DEL MODELO GLOBAL")
print("="*50)
print(f"Tiempo de Servicio Ponderado Teórico: {t_servicio_ponderado:.2f} min")
print(f"Tiempo entre Llegadas Ponderado Teórico: {t_llegada_ponderado:.2f} min")
print(f"Factor de Utilización Real por Cajero (Rho): {lambda_por_cajero/mu_global:.4f} ({lambda_por_cajero/mu_global*100:.1f}%)")
print("="*50 + "\n")

# 2. MOTOR DE SIMULACIÓN DE EVENTOS DISCRETOS

def simular_cajero_parqueadero(num_clientes=2500):
    tiempo_actual = 0.0
    tiempos_llegada = []
    tiempos_servicio_asignados = []
    tipos_usuario = []

    # Generación de arribos exponenciales y asignación estocástica de perfiles
    for _ in range(num_clientes):
        tiempo_actual += np.random.exponential(1.0 / lambda_por_cajero)
        tiempos_llegada.append(tiempo_actual)

        # Selección del tipo de cliente basado en probabilidades empíricas
        tipo = np.random.choice([0, 1, 2, 3], p=PROBS)
        tipos_usuario.append(tipo)

        # Asignación de tiempo de servicio con distribución exponencial según tipo
        tiempos_servicio_asignados.append(np.random.exponential(TIEMPOS_SERVICIO[tipo]))

    # Vectores de salida de la fila (Verificación lógica)
    tiempo_inicio_servicio = np.zeros(num_clientes)
    tiempo_fin_servicio = np.zeros(num_clientes)
    tiempos_espera_cola = np.zeros(num_clientes)
    tiempos_en_sistema = np.zeros(num_clientes)

    for i in range(num_clientes):
        if i == 0:
            tiempo_inicio_servicio[i] = tiempos_llegada[i]
        else:
            # Regla de oro de verificación: No puede iniciar antes de llegar ni antes de que se libere el anterior
            tiempo_inicio_servicio[i] = max(tiempos_llegada[i], tiempo_fin_servicio[i-1])

        tiempos_espera_cola[i] = tiempo_inicio_servicio[i] - tiempos_llegada[i]
        tiempo_fin_servicio[i] = tiempo_inicio_servicio[i] + tiempos_servicio_asignados[i]
        tiempos_en_sistema[i] = tiempo_fin_servicio[i] - tiempos_llegada[i]

    return tiempos_espera_cola, tiempos_en_sistema, tiempos_servicio_asignados, tipos_usuario

# Simulación independiente de los 3 cajeros del Supercentro
cajeros_resultados = {}
for c in range(1, num_cajeros + 1):
    wq, w, s, t = simular_cajero_parqueadero(num_clientes=2500)
    cajeros_resultados[f'Cajero_{c}'] = {'Wq': wq, 'W': w, 'Servicio': s, 'Tipos': t}

# 3. ELIMINACIÓN DEL ESTADO TRANSITORIO

punto_corte = 400  # Período de calentamiento (Warm-up) determinado gráficamente

def calcular_promedio_movil(datos, ventana=150):
    return np.convolve(datos, np.ones(ventana)/ventana, mode='valid')

datos_crudos_w = cajeros_resultados['Cajero_1']['W']
datos_suavizados_w = calcular_promedio_movil(datos_crudos_w, ventana=150)

# --- GRÁFICA: ANTES Y DESPUÉS DEL ESTADO TRANSITORIO ---
fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(16, 6))

# Lado Izquierdo: Identificación del Transitorio
ax1.plot(datos_crudos_w, color='gray', alpha=0.3, label='Tiempo W Individual')
ax1.plot(range(149, len(datos_crudos_w)), datos_suavizados_w, color='blue', linewidth=2, label='Promedio Móvil (Ventana 150)')
ax1.axvline(x=punto_corte, color='red', linestyle='--', linewidth=2, label=f'Corte de Calentamiento ({punto_corte} cl)')
ax1.set_title('Antes: Identificación del Período Transitorio')
ax1.set_xlabel('Índice de Cliente')
ax1.set_ylabel('Tiempo en Sistema (Minutos)')
ax1.legend()
ax1.grid(True, alpha=0.3)

# Lado Derecho: Estado Estable Puro (Después)
datos_estables_w = datos_crudos_w[punto_corte:]
ax2.plot(range(punto_corte, len(datos_crudos_w)), datos_estables_w, color='green', alpha=0.4, label='Datos en Estado Estable')
ax2.axhline(y=np.mean(datos_estables_w), color='darkgreen', linestyle='-', linewidth=2.5, label=f'Media Estable: {np.mean(datos_estables_w):.2f} min')
ax2.set_title('Después: Sistema purgado en Estado Estable')
ax2.set_xlabel('Índice de Cliente')
ax2.set_ylabel('Tiempo en Sistema (Minutos)')
ax2.legend()
ax2.grid(True, alpha=0.3)

plt.tight_layout()
plt.show()

